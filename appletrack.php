                                <h1 class="sn-title">Apple can track users even if the location setting is off</h1>
                                
                                <p>
                                Apple recently filed a class action lawsuit alleging it recorded users' mobile activity without their consent and despite privacy guarantees, thus violating the California Privacy Act.
                                The lawsuit filed by New York citizen and iPhone 13 owner Elliot Libman accuses Apple of lying about users controlling what information they share when using the iPhone's built-in apps.

                                <br>
                                    </br>
                                    The lawsuit is also supported by a Gizmodo report discussing security researchers at software company Mysk. Earlier this month, researchers Tommy Mysk and Talal Haj Bakry claimed to have found evidence that analytics controls and anti-tracking settings had no discernible effect on Apple's collection of data in its default apps.
                                For example, the App Store app continuously collects usage data in real time, including user taps, apps searched for, views on ads, and how long the user is looking at certain apps. In addition, Apple may also collect typical details of the device's fingerprint method, including ID number, device model, screen resolution, installed keyboard language, and internet connection type.
                                <p>
                                Reporting from Mashabel, Mysk came to this conclusion after testing using a jailbroken iPhone running iOS 14.6. The team found iPhone activity similar to that of non-jailbroken phones running iOS 16. However, because of encryption, Mysk was unable to determine exactly what data was being sent from his iPhone device.
