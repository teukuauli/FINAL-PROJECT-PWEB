                                <h1 class="sn-title">analog tv will stop operating on december 20th</h1>
                                <p>
                                The next phase of Analog TV Shutdown or Analog Switch Off (ASO) will be held in Surabaya and its surroundings which are part of the East Java-1 region on Tuesday (20/12) at 24.00.

"Broadcasting institutions have agreed to stop broadcasting analog to digital TV broadcasts on December 20, 2022 at 24.00 for the East Java-1 region," said Usman Kansong, Director General of Information and Public Communication of the Ministry of Communication and Informatics (Kominfo), in a statement.
                                <p>
                                The distribution of set top boxes (STB) for poor families in East Java has reached 100% before December 20, 2022," he explained

The agreement to implement the ASO was welcomed by Kominfo. Kominfo also welcomes the commitment of STB and digital TV providers regarding the availability of these supporting devices.

"Kominfo welcomes the broadcasters' agreement to stop analog broadcasts (ASO) on December 20, 2022 at 24.00," said Usman.
                                <p>
                                "The Ministry of Communication and Informatics also welcomes the commitment of the Electronics Entrepreneurs Association (Gabel), which is an association of STB and digital TV vendors, to guarantee the availability of STB in the market at affordable prices," he continued.

For this plan, Kominfo urges the public to immediately fulfill their needs to be able to enjoy digital TV broadcasts, one of which is STB if the TV in their house does not yet support DVB-T2 technology.

"Kominfo encourages people in East Java-1 whose TVs are still analog to equip with STB from now on so that by the time of ASO 2 December 2022 they are ready to enjoy digital TV broadcasts with clean pictures, clear sound and sophisticated technology," he said.

Prior to this, the ASO stage after the November 2 deadline had been held in several cities, including Bandung, Yogya, Solo, to Batam.
